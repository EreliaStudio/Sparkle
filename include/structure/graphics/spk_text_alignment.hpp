#pragma once

namespace spk
{
	/**
	 * @enum HorizontalAlignment
	 * @brief Specifies horizontal alignment options.
	 * 
	 * This enumeration is used to define the horizontal positioning of elements.
	 */
	enum class HorizontalAlignment
	{
		Left,	  ///< Align to the left.
		Centered,  ///< Center align.
		Right	  ///< Align to the right.
	};

	/**
	 * @enum VerticalAlignment
	 * @brief Specifies vertical alignment options.
	 * 
	 * This enumeration is used to define the vertical positioning of elements.
	 */
	enum class VerticalAlignment
	{
		Top,	   ///< Align to the top.
		Centered,  ///< Center align.
		Down	   ///< Align to the bottom.
	};
}
